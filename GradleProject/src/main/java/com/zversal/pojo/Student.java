package com.zversal.pojo;

public class Student {
	
	private int id;
	private String name;
	private int age;


	public int getId()
	{
		return this.id;
	}

	public String getName()
	{
		return this.name;
	}

	public int getAge()
	{
		return this.age;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public void setName(String s)
	{
		this.name = s;
	}

	public void setAge(int age)
	{
		this.age = age;
	}

}
